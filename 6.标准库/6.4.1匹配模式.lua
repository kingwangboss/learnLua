local text = "Lua is a scripting language. It is easy to learn and use."

-- 匹配单词"Lua"
local match1 = string.match(text, "%w+")
print("匹配到的单词:", match1)

-- 匹配句子中的首字母大写单词
local match2 = string.match(text, "%u%l+")
print("匹配到的首字母大写单词:", match2)

-- 替换文本中的"Lua"为"Lua programming"
local replaced_text = string.gsub(text, "Lua", "Lua programming")
print("替换后的文本:", replaced_text)

-- 1基本模式：
-- . 匹配任意单个字符。
-- %a 匹配任意字母字符。
-- %d 匹配任意数字字符。
-- %s 匹配任意空白字符（空格、制表符、换行符等）。
-- %w 匹配任意字母或数字字符。
-- %x 匹配任意十六进制字符。

-- 2重复次数：
-- * 匹配前一个字符0次或多次。
-- + 匹配前一个字符1次或多次。
-- - 匹配前一个字符0次或1次。
-- ? 匹配前一个字符0次或1次。
-- {n} 匹配前一个字符恰好n次。
-- {n,} 匹配前一个字符至少n次。
-- {n,m} 匹配前一个字符至少n次但不超过m次。

-- 3字符类：
-- [...] 匹配方括号内的任意字符。
-- [^...] 匹配除了方括号内的字符以外的任意字符。

-- 4锚点：
-- ^ 匹配字符串的开头。
-- $ 匹配字符串的结尾。
-- %bxy 匹配以x开始，以y结尾的字符串，其中x和y是任意字符。

-- 5捕获与替换：
-- () 用于捕获子串，可以在匹配模式中使用捕获的子串进行后续处理。
-- %1, %2, ... 用于在替换字符串中引用捕获的子串。

-- 6特殊字符转义：
-- % 用于转义特殊字符，例如 %% 匹配百分号字符 %。